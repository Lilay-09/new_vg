import {
  faAngleDown,
  faAngleRight,
  faBars,
  faCaretDown,
  faSearch,
  faXmark,
} from "@fortawesome/free-solid-svg-icons";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import Head from "next/head";
import Image from "next/image";
import { useRouter } from "next/router";
import React, { useEffect, useMemo, useRef, useState } from "react";
import ActiveLink from "../components/ActiveLink";
import BtnComp from "../components/BtnComp";
import styles from "../styles/Header/Header.module.css";

const Header = ({ title }) => {
  const router = useRouter();
  const [isOpenMenu, setOpenMenu] = useState(false);
  const menuRef = useRef();
  const xREf = useRef();

  const [scrolled, setScrolled] = useState(false);

  const handleScroll = () => {
    const offset = window.scrollY;

    if (offset > 200) {
      setScrolled(true);
    } else {
      setScrolled(false);
    }
  };
  const menuHandler = () => {
    setOpenMenu((prev) => !prev);
  };
  useEffect(() => {
    isOpenMenu
      ? (document.body.style.overflow = "hidden")
      : (document.body.style.overflow = "auto");
  });
  useEffect(() => {
    let handleOpenMenu = (e) => {
      if (
        !xREf.current.contains(e.target) &&
        !menuRef.current.contains(e.target)
      ) {
        setOpenMenu(false);
      }
    };

    document.addEventListener("mousedown", handleOpenMenu, true);
    return () => {
      document.removeEventListener("mousedown", handleOpenMenu, true);
    };
  }, []);
  const [openProject, setOpenProject] = useState(false);
  const [menuDropDown, setMenuDropDown] = useState(false);
  const projectOnClickHandler = () => {
    setOpenProject(!openProject);
  };
  const href = "/projects";
  return (
    <div>
      <Head>
        <title>{title}</title>
        <link rel="icon" href="/logo.png" />
      </Head>
      {/* <meta name="description" content="Generated by create next app" /> */}
      <nav
        className={
          router.asPath === "/"
            ? styles.navbar_container
            : styles.navbar_container_relative
        }
        id="#navbar"
      >
        <div className={styles.navbar}>
          <div className={styles.navbar_logo} onClick={() => router.push("/")}>
            <Image
              alt="logo"
              src="/images/logo2.png"
              width={200}
              height={200}
              priority
            />
          </div>
          <div className={styles.navbar_link}>
            <ActiveLink href="/" id="home">
              Home
            </ActiveLink>
            <ActiveLink href="/our-team">Our Team</ActiveLink>
            <ActiveLink href="/our-services">Our Services</ActiveLink>
            <ActiveLink href="/properties">Our Properties</ActiveLink>
            <ActiveLink href="/about-us">About Us</ActiveLink>
            <ActiveLink href="/careers">Careers</ActiveLink>
            <ActiveLink href="/contact-us">Contact Us</ActiveLink>
          </div>
          <div style={{ flexGrow: "1" }}></div>
          <div className={styles.get_intouch}>
            <FontAwesomeIcon
              icon={!isOpenMenu ? faBars : faXmark}
              width={20}
              height={20}
              className={`menuBar ${
                router.asPath === "/" ? "inactive" : "black"
              } ${isOpenMenu && "active"}`}
              onClick={menuHandler}
              ref={xREf}
            />
          </div>
        </div>
      </nav>
      <div className={`menu_drop_down ${isOpenMenu && "active"}`}>
        <div className={`menu_list ${isOpenMenu && "active"}`} ref={menuRef}>
          <div className="menu__list_frame">
            <ActiveLink href="/" black>
              Home
            </ActiveLink>
            <ActiveLink href="/our-team" black>
              Our Team
            </ActiveLink>
            <ActiveLink href="/our-services" black>
              Our Services
            </ActiveLink>
            <ActiveLink href="/properties" black>
              Our Properties
            </ActiveLink>
            <ActiveLink href="/about-us" black>
              About Us
            </ActiveLink>
            <ActiveLink href="/careers" black>
              Careers
            </ActiveLink>
            <ActiveLink href="/contact-us" black>
              Contact Us
            </ActiveLink>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Header;
